left = input("Left-hand side: ")
right = input("Right-hand side: ")
print(str(left) + " < " + str(right) + " is " + str(int(left) < int(right)))
print(str(left) + " >= " + str(right) + " is " + str(int(left) >= int(right)))
print(str(left) + " == " + str(right) + " is " + str(left == right))
print(str(left) + " != " + str(right) + " is " + str(left != right))